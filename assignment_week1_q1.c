#include<stdio.h>
int main(){
    int a,b;
    printf("Enter two numbers\n");
    scanf("%d %d",&a,&b);
    printf("Sum of two numbers is %d\n",a+b);
    printf("difference of two numbers is %d\n",a-b);
    printf("multiplication of two numbers is %d\n",a*b);
    printf("division of two numbers is %d\n",a/b);
    printf("Modulo of two numbers is %d\n",a%b);
    return 0;
}